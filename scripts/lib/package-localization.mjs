function applyParts(text, parts) {
  if (!text) return text;
  const entries = Object.entries(parts).sort(
    (a, b) => b[0].length - a[0].length,
  );
  let result = text;
  for (const [from, to] of entries) {
    const re = new RegExp(from.replace(/[.*+?^${}()|[\]\\]/g, "\\$&"), "gi");
    result = result.replace(re, to);
  }
  return result;
}

export function localizeCollectionTitle(title, locale) {
  return COL_TITLE_MAP[locale]?.[title] || title;
}

export function normalizePackagePriceList(priceList) {
  const keyMap = { fullDay: "1D" };
  return Object.fromEntries(
    Object.entries(priceList).map(([k, v]) => [keyMap[k] || k, v]),
  );
}

export function getPriceListLabels(priceListType, locale) {
  return PRICE_LIST_LABELS[priceListType]?.[locale];
}

export function localizeBoatSpecs(specs, locale) {
  if (locale === "id") return specs;
  const labelMap = BOAT_SPECS_LABELS[locale] || {};
  const valueMap = BOAT_VALUES_MAP[locale] || {};
  return specs.map((s) => ({
    label: labelMap[s.label] || s.label,
    value: valueMap[s.value] || s.value,
  }));
}

export function localizeCabins(cabins, locale) {
  if (locale === "id") return cabins;
  const partsMap = CABIN_PARTS[locale] || CABIN_PARTS.en;
  const unitMap = PRICE_UNIT[locale] || PRICE_UNIT.en;
  return cabins.map((c) => ({
    ...c,
    name: Object.entries(CABIN_EN).reduce(
      (name, [id, en]) => name.replace(id, en),
      c.name,
    ),
    description: applyParts(c.description, partsMap),
    prices: c.prices
      ? Object.fromEntries(
          Object.entries(c.prices).map(([k, v]) => {
            let price = v;
            for (const [from, to] of Object.entries(unitMap)) {
              price = price.split(from).join(to);
            }
            return [k, price];
          }),
        )
      : undefined,
  }));
}
